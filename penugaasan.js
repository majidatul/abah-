
// +=
let b = 12;
b += 4; // b = 12 + 4
console.log(b); // 

// -=
let c = 8;
c -= 2; // c = 8 - 2
console.log(c); // 

// *=
let d = 12;
d *= 2; // d = 12 * 2
console.log(d); // 

// /=
let e = 8;
e /= 2; // e = 8 / 2
console.log(e); // 

// %=
let f = 5;
f %= 2; // f = 5 % 2
console.log(f); // 

// **=
let g = 10;
g **= 2; // g = 10 ** 2
console.log(g); // 